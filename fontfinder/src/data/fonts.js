// Helper: get fonts by category
export function getFontsByCategory(categoryId) {
  if (!categoryId || categoryId === 'all') return FONTS;
  return FONTS.filter(f => f.category === categoryId);
}

// Helper: search fonts
export function searchFonts(query, categoryId = 'all') {
  const q = query.toLowerCase().trim();
  const base = getFontsByCategory(categoryId);
  if (!q) return base;
  return base.filter(f =>
    f.name.toLowerCase().includes(q) ||
    f.tags.some(t => t.includes(q)) ||
    f.category.includes(q) ||
    f.desc.toLowerCase().includes(q)
  );
}
